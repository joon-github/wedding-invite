const fs = require("fs");
const { Client } = require("pg");

const lines = fs.readFileSync(".env.local", "utf8").split("\n");
const env = {};
for (const line of lines) {
  const m = line.match(/^([^=]+)="(.*)"$/);
  if (m) env[m[1]] = m[2];
}

async function run() {
  const client = new Client({ connectionString: env.POSTGRES_URL });
  await client.connect();

  await client.query(`
    CREATE TABLE IF NOT EXISTS public.quiz_questions (
      id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      created_at timestamptz NOT NULL DEFAULT now(),
      question text NOT NULL,
      image text,
      options jsonb NOT NULL DEFAULT '[]',
      answer integer NOT NULL DEFAULT 0,
      sort_order integer NOT NULL DEFAULT 0
    );
  `);
  console.log("quiz_questions table created");

  const { rows } = await client.query("SELECT count(*) as cnt FROM public.quiz_questions");
  if (rows[0].cnt === "0") {
    const questions = [
      { question: "범준이와 정아가 처음 만난 곳은 어디일까요?", image: null, options: ["소개팅", "회사", "대학교", "동네 카페"], answer: 0, sort_order: 1 },
      { question: "두 사람이 처음 함께 여행한 곳은?", image: null, options: ["제주도", "부산", "강릉", "일본"], answer: 2, sort_order: 2 },
      { question: "범준이가 프로포즈한 장소는?", image: null, options: ["한강공원", "집", "레스토랑", "여행지"], answer: 1, sort_order: 3 },
      { question: "정아가 가장 좋아하는 범준이의 모습은?", image: null, options: ["요리할 때", "운동할 때", "웃을 때", "집중할 때"], answer: 2, sort_order: 4 },
      { question: "웨딩 사진에서 강아지는 총 몇 번 등장했을까요?", image: "/image/A7402503.jpeg", options: ["0번", "1번", "2번", "3번"], answer: 2, sort_order: 5 },
    ];

    for (const q of questions) {
      await client.query(
        "INSERT INTO public.quiz_questions (question, image, options, answer, sort_order) VALUES ($1, $2, $3, $4, $5)",
        [q.question, q.image, JSON.stringify(q.options), q.answer, q.sort_order]
      );
    }
    console.log("Seeded 5 initial questions");
  } else {
    console.log("Questions already exist, skipping seed");
  }

  await client.end();
}

run().catch((e) => console.error(e.message));
